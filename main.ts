function start () {
    leveldif = game.askForString("Noob or Expert. n for noob and e for expert")
    if (leveldif == "n") {
        while (info.score() == 0) {
            Lvl1()
            pause(15000)
            if (info.score() >= 1) {
                game.gameOver(true)
                game.setGameOverEffect(true, effects.confetti)
            } else {
                info.changeLifeBy(-1)
                sprites.destroy(Bird1)
            }
        }
    } else if (leveldif == "e") {
        while (info.score() == 0) {
            Level2()
            pause(15000)
            if (info.score() >= 1) {
                game.gameOver(true)
                game.setGameOverEffect(true, effects.confetti)
                pause(2000)
                game.showLongText("Congrats you have finished the game", DialogLayout.Bottom)
            } else {
                info.changeLifeBy(-1)
                sprites.destroy(Bird1)
            }
        }
    } else {
        game.splash("buddy, that aint what we asked for")
        start()
    }
}
